import { execFileSync } from "node:child_process";
import {
  cpSync,
  existsSync,
  mkdtempSync,
  mkdirSync,
  readFileSync,
  readdirSync,
  renameSync,
  rmSync,
  writeFileSync,
} from "node:fs";
import { tmpdir } from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";

export function parseTauriCommandNames(projectRoot) {
  const registryPath = path.join(
    projectRoot,
    "src-tauri/src/commands/registry.rs",
  );
  const source = readFileSync(registryPath, "utf8");
  const productionSource = source.split("#[cfg(test)]")[0];
  const listMarker = "$macro! {";
  const listStart = productionSource.indexOf(listMarker);
  if (listStart === -1) {
    throw new Error(
      "Could not find with_commands command list in commands/registry.rs",
    );
  }
  const bodyContentStart = listStart + listMarker.length;
  const listEnd = productionSource.indexOf("\n        }", bodyContentStart);
  if (listEnd === -1) {
    throw new Error("Could not find with_commands command list end");
  }
  const block = productionSource.slice(bodyContentStart, listEnd);
  return block
    .split("\n")
    .map((line) => line.trim().replace(/[),]+$/, ""))
    .map((line) => line.split(",").pop()?.trim() ?? "")
    .filter((name) => /^[a-z][a-z0-9_]*$/.test(name));
}

export function buildTauriCommandNamesSource(commandNames) {
  const union = commandNames.map((name) => `  | "${name}"`).join("\n");
  return `// Generated by scripts/generate-bindings.mjs\nexport const TAURI_COMMAND_NAMES = [\n${commandNames.map((name) => `  "${name}",`).join("\n")}\n] as const;\n\nexport type TauriCommandName =\n${union};\n`;
}

export function buildGeneratedBarrelSource(typeFiles) {
  const exports = typeFiles.map((name) => {
    const typeName = path.basename(name, ".ts");
    return `export type { ${typeName} } from "./bindings/${typeName}";`;
  });

  return `// Generated by scripts/generate-bindings.mjs\n${exports.join("\n")}\nexport type { TauriCommandName } from "./tauri-command-names";\n`;
}

export function writeFileAtomic(filePath, content) {
  const tempPath = path.join(
    path.dirname(filePath),
    `.${path.basename(filePath)}.${process.pid}.${Date.now()}.tmp`,
  );
  writeFileSync(tempPath, content, "utf8");
  renameSync(tempPath, filePath);
}

export function replaceDirectoryWithBackup(targetDir, sourceDir) {
  const parentDir = path.dirname(targetDir);
  const baseName = path.basename(targetDir);
  const suffix = `${process.pid}.${Date.now()}`;
  const stagingDir = path.join(parentDir, `.${baseName}.next.${suffix}`);
  const backupDir = path.join(parentDir, `.${baseName}.backup.${suffix}`);
  let backupCreated = false;

  rmSync(stagingDir, { recursive: true, force: true });
  rmSync(backupDir, { recursive: true, force: true });
  cpSync(sourceDir, stagingDir, { recursive: true });

  try {
    if (existsSync(targetDir)) {
      renameSync(targetDir, backupDir);
      backupCreated = true;
    }

    renameSync(stagingDir, targetDir);
    rmSync(backupDir, { recursive: true, force: true });
  } catch (error) {
    rmSync(targetDir, { recursive: true, force: true });
    if (backupCreated) {
      renameSync(backupDir, targetDir);
    }
    rmSync(stagingDir, { recursive: true, force: true });
    throw error;
  }
}

export function commitGeneratedBindings({
  bindingsDir,
  tempBindingsDir,
  barrelPath,
  commandNamesPath,
  typeFiles,
  commandNames,
}) {
  replaceDirectoryWithBackup(bindingsDir, tempBindingsDir);
  writeFileAtomic(barrelPath, buildGeneratedBarrelSource(typeFiles));
  writeFileAtomic(commandNamesPath, buildTauriCommandNamesSource(commandNames));
}

function formatGeneratedBindings(projectRoot, generatedDir) {
  const prettierPath = path.join(projectRoot, "node_modules/.bin/prettier");
  if (!existsSync(prettierPath)) {
    return;
  }

  const relativeGeneratedDir = path
    .relative(projectRoot, generatedDir)
    .split(path.sep)
    .join("/");
  execFileSync(prettierPath, ["--write", relativeGeneratedDir], {
    cwd: projectRoot,
    stdio: "inherit",
  });
}

export function runGenerateBindings(projectRoot) {
  const generatedDir = path.join(projectRoot, "src/types/generated");
  const bindingsDir = path.join(generatedDir, "bindings");
  const barrelPath = path.join(generatedDir, "index.ts");
  const commandNamesPath = path.join(generatedDir, "tauri-command-names.ts");
  const tauriBindingsDir = path.join(projectRoot, "src-tauri/bindings");
  const tempRoot = mkdtempSync(path.join(tmpdir(), "bpp-bindings-"));
  const tempBindingsDir = path.join(tempRoot, "bindings");

  mkdirSync(generatedDir, { recursive: true });
  mkdirSync(tempBindingsDir, { recursive: true });

  try {
    execFileSync(
      "cargo",
      [
        "test",
        "export_bindings",
        "--manifest-path",
        "src-tauri/Cargo.toml",
        "--",
        "--nocapture",
      ],
      {
        cwd: projectRoot,
        stdio: "inherit",
        env: {
          ...process.env,
          TS_RS_EXPORT_DIR: tempBindingsDir,
          TS_RS_LARGE_INT: "number",
        },
      },
    );

    const typeFiles = readdirSync(tempBindingsDir)
      .filter((name) => name.endsWith(".ts"))
      .sort((left, right) => left.localeCompare(right));

    if (typeFiles.length === 0) {
      throw new Error("No TypeScript bindings were generated");
    }

    commitGeneratedBindings({
      bindingsDir,
      tempBindingsDir,
      barrelPath,
      commandNamesPath,
      typeFiles,
      commandNames: parseTauriCommandNames(projectRoot),
    });
    formatGeneratedBindings(projectRoot, generatedDir);
  } finally {
    rmSync(tempRoot, { recursive: true, force: true });
    rmSync(tauriBindingsDir, { recursive: true, force: true });
  }
}

const scriptPath = fileURLToPath(import.meta.url);
const invokedAsScript =
  process.argv[1] && path.resolve(process.argv[1]) === scriptPath;

if (invokedAsScript) {
  runGenerateBindings(path.resolve(path.dirname(scriptPath), ".."));
}
